package com.fr.station.common.bean.system;

import java.util.List;

/**
 * This bean mainly to send-receive data between client and server.
 * 
 * @author henry
 */
public class MenuItem {

	// ------- Constants (static final) ----------------------------------------

	// ------- Static Variables (static) ---------------------------------------

	// ------- Instance Variables (private) ------------------------------------

	private int id;

	private String url;

	private String name;

	private MenuItem parentMenu;
	
	private int parentId;

	private List<MenuItem> children;

	// ------- Constructors ----------------------------------------------------

	// ------- Instance Methods (public) ---------------------------------------

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public MenuItem getParentMenu() {
		return parentMenu;
	}

	public void setParentMenu(MenuItem parentMenu) {
		this.parentMenu = parentMenu;
	}

	public List<MenuItem> getChildren() {
		return children;
	}

	public void setChildren(List<MenuItem> children) {
		this.children = children;
	}

	public int getParentId() {
		return parentId;
	}

	public void setParentId(int parentId) {
		this.parentId = parentId;
	}

	@Override
	public String toString() {
		return "MenuItem [url=" + url + ", name=" + name + "]";
	}
	

	// ------- Instance Methods (protected) ------------------------------------

	// ------- Instance Methods (private) --------------------------------------
}
